# Read full dump (which has // DECODE ERROR for bad lines)
with open(dump_path, 'r', encoding='utf-8') as f:
    lines = f.readlines()

# Reconstruct
data = {}
for line in lines:
    line = line.strip()
    if line.startswith('{') or line.startswith('}') or not line:
        continue
    if line.startswith('// DECODE ERROR'):
        continue
    
    # Try to parse key-value
    try:
        # Line looks like: "key": "value",
        if line.endswith(','):
            line = line[:-1]
        
        # We can use json.loads to parse it if we wrap it in braces
        item = json.loads('{' + line + '}')
        data.update(item)
    except Exception as e:
        print(f"Skipping bad line: {line}")

# Apply fixes
data.update(fixed_systems)

# Sort keys like in en_v2.json if possible (optional but good)
